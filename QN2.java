
public class QN2 {
	
	public static void main(String[] args) {
		
		int n = 8;
		char ch = 'c';
		float n2 = 8.27f;
		boolean b = true;
		double d = 2.45384737;
		
		System.out.println("Integer: "+n);
		System.out.println("Character: "+ch);
		System.out.println("Float Number: "+n2);
		System.out.println("Doubled Number: "+d);
		System.out.println("Boolean(true or false): "+b);
		
	}

}
